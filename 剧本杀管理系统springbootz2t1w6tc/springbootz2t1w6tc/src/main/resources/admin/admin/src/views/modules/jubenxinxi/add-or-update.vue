				if (data && data.code === 0) { 
				       if(data.data.total>=crossoptnum) {
					     this.$message.error(this.$storage.get('tips'));
					       return false;
				       } else {
					 this.$http({
					   url: `jubenxinxi/${!this.ruleForm.id ? "save" : "update"}`,
					   method: "post",
